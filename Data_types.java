

public class Data_types {
    public static void main(String[] args) {
        byte a=10;
        short b=20;
        int c=30;
        long d=40;
        // float e=2.3f;
        double f=20.3;
        char ch='A';
        boolean val=true;
        System.out.println("Byte: "+a+"\n Short: "+b+"\n int: "+c+"\n long: "+d+"\n float: "+f+"\n double: "+d+"\n char: "+ch+"\n boolean: "+val);
    }
}
